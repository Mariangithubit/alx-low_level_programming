#include "3-calc.h"
/**
 * op_add - sum two nums
 * @a: num 1
 * @b: num 2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two nums
 * @a: num 1
 * @b: num 2
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mutipule two nums
 * @a: num 1
 * @b: num 2
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two nums
 * @a: num 1
 * @b: num 2
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *  op_mod - module two nums
 * @a: num 1
 * @b: num 2
 * Return: the remainder of the division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
